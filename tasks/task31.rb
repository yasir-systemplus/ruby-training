#Given two arguments, return an array which contains these two arguments.

def make_pair(a, b)
    [a, b]
end


print make_pair(1, 2) #➞ [1, 2]
print make_pair(51, 21) #➞ [51, 21]
print make_pair(512124, 215) #➞ [512124, 215]